package AdminDashboard;

public class Customer {
    private String name, phone, password, status, expectedArrivalTime, startingPoint, destination;
    private int capacity;

    public Customer(String name, String phone, String password, String status, String expectedArrivalTime, int capacity, String startingPoint, String destination) {
        this.name = name;
        this.phone = phone;
        this.password = password;
        this.status = status;
        this.expectedArrivalTime = expectedArrivalTime;
        this.startingPoint = startingPoint;
        this.destination = destination;
        this.capacity = capacity;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getExpectedArrivalTime() {
        return expectedArrivalTime;
    }

    public void setExpectedArrivalTime(String expectedArrivalTime) {
        this.expectedArrivalTime = expectedArrivalTime;
    }

    public String getStartingPoint() {
        return startingPoint;
    }

    public void setStartingPoint(String startingPoint) {
        this.startingPoint = startingPoint;
    }

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }
}
